    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int confirm = JOptionPane.showConfirmDialog(null, "Â¿Realmente desea eliminar el registro?", "Eliminar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (confirm == 0) {
            ClienteDAO clDao2 = new ClienteDAO();
            clDao2.eliminarCliente(txtRut.getText());
            txtRut.setEnabled(true);
            txtRut.setText("");
            lblNombre.setText("");
        } else {
            txtRut.setEnabled(true);
            txtRut.setText("");
            lblNombre.setText("");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

